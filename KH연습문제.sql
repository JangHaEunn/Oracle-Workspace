FROM EMPLOYEE
WHERE MOD(EMP_ID, 2) = 1;

-- 19.
SELECT *
FROM EMPLOYEE
WHERE EXTRACT(YEAR FROM SYSDATE) - EXTRACT(YEAR FROM HIRE_DATE) >= 20;

-- 20.
SELECT EMP_NAME, TO_CHAR(SALARY, 'L9,999,999') 급여
FROM EMPLOYEE;

-- 21.
SELECT EMP_NAME, NVL(DEPT_CODE, '부서없음') 부서코드, TO_CHAR(TO_DATE(SUBSTR(EMP_NO,1,6)),'YY"년" MM"월" DD"일"') AS 생년월일,
    EXTRACT(YEAR FROM SYSDATE) - EXTRACT(YEAR FROM TO_DATE(SUBSTR(EMP_NO,1,2), 'RRRR'))|| '세' AS 나이,
    TRUNC(MONTHS_BETWEEN(TRUNC(SYSDATE), TO_DATE(SUBSTR(EMP_NO,1,6), 'RRMMDD')) / 12)
    
FROM EMPLOYEE;

-- 22.
SELECT EMP_NAME,DECODE(DEPT_CODE, 'D5', '총무부', 'D6', '기획부', 'D9', '영업부') AS 부서코드
FROM EMPLOYEE
WHERE DEPT_CODE IN ('D5', 'D6' ,'D9')
ORDER BY 부서코드;

-- 23.
SELECT EMP_NAME, SUBSTR(EMP_NO, 1,6) 사, SUBSTR(EMP_NO, 8,14),
        SUBSTR(EMP_NO, 1,6)+SUBSTR(EMP_NO, 8,14) AS 합
FROM EMPLOYEE
WHERE EMP_ID= '201';

-- 24.
SELECT SUM((SALARY+ SALARY*NVL(BONUS,0))*12) AS "연봉 합"
FROM EMPLOYEE
WHERE DEPT_CODE = 'D5';

-- 25.
SELECT EXTRACT(YEAR FROM HIRE_DATE) 입사년도, COUNT(*) AS "입사 인원수"
FROM EMPLOYEE
GROUP BY EXTRACT(YEAR FROM HIRE_DATE)
ORDER BY EXTRACT(YEAR FROM HIRE_DATE);
