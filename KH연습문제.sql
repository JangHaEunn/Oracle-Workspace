-- 8.
SELECT EMP_NAME, SALARY, (SALARY + SALARY*NVL(BONUS,0))*12-((SALARY*12)*0.3) AS 실수령액, HIRE_DATE
FROM EMPLOYEE
WHERE (SALARY + SALARY*NVL(BONUS,0))*12-((SALARY*12)*0.3) >= 50000000;

-- 9.
