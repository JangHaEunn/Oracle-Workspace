-- 15.
SELECT EMP_NAME, (SUBSTR(EMP_NO,1,2)) AS 생년, (SUBSTR(EMP_NO,3,2)) AS 생월, (SUBSTR(EMP_NO,5,2)) AS 생일
FROM EMPLOYEE;

-- 16.
SELECT EMP_NAME, RPAD(SUBSTR(EMP_NO, 1, 7),14,'*')
FROM EMPLOYEE;

-- 17.
SELECT EMP_NAME, 
    FLOOR(ABS(HIRE_DATE - SYSDATE)) AS 근무일수1,
    FLOOR(SYSDATE - HIRE_DATE) AS 근무일수2
FROM EMPLOYEE;

-- 18.
